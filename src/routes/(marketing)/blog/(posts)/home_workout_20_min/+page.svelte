<h3>Pre-Workout Warm-Up (5 minutes)</h3>

<p>
  Before starting the main workout, do these movements to prepare your body:
</p>

<ol>
  <li><strong>Arm circles:</strong> 30 seconds forward, 30 seconds backward</li>
  <li><strong>Bodyweight squats:</strong> 10 slow, controlled reps</li>
  <li><strong>Cat-cow stretches:</strong> 30 seconds</li>
  <li><strong>Light jogging in place:</strong> 60 seconds</li>
  <li><strong>Hip circles:</strong> 30 seconds each direction</li>
</ol>

<h3>Post-Workout Cool-Down (5 minutes)</h3>

<p>After completing the workout, take time to stretch:</p>

<ol>
  <li><strong>Chest stretch:</strong> 30 seconds each side</li>
  <li><strong>Quad stretch:</strong> 30 seconds each leg</li>
  <li><strong>Hamstring stretch:</strong> 30 seconds each leg</li>
  <li><strong>Child's pose:</strong> 60 seconds</li>
  <li><strong>Deep breathing:</strong> 60 seconds</li>
</ol>

