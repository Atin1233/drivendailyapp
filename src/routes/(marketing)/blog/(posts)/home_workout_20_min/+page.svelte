<h3>The Workout Structure</h3>

<p>
  This is a circuit-style workout with 5 exercises. You'll do each exercise for
  40 seconds, rest for 20 seconds, then move to the next exercise. Complete 4
  rounds for a total of 20 minutes.
</p>

<blockquote>
  <p>
    "The best workout is the one you'll actually do. This routine fits into any
    schedule and requires zero equipment."
  </p>
</blockquote>

<h3>The 5 Exercises</h3>

<h4>1. Push-Ups (40 seconds)</h4>
<p>
  <strong>Target:</strong> Chest, shoulders, triceps<br />
  <strong>Form:</strong> Start in plank position, lower your body until your
  chest nearly touches the floor, then push back up<br />
  <strong>Modification:</strong> Do knee push-ups or wall push-ups if needed
</p>

<h4>2. Squats (40 seconds)</h4>
<p>
  <strong>Target:</strong> Quads, glutes, hamstrings<br />
  <strong>Form:</strong> Stand with feet shoulder-width apart, lower your body
  as if sitting in a chair, then stand back up<br />
  <strong>Modification:</strong> Hold onto a chair for balance if needed
</p>

<h4>3. Mountain Climbers (40 seconds)</h4>
<p>
  <strong>Target:</strong> Core, shoulders, cardiovascular system<br />
  <strong>Form:</strong> Start in plank position, alternate bringing knees
  toward chest in a running motion<br />
  <strong>Modification:</strong> Slow down the pace or do standing knee raises
</p>

<h4>4. Lunges (40 seconds)</h4>
<p>
  <strong>Target:</strong> Quads, glutes, hamstrings, balance<br />
  <strong>Form:</strong> Step forward with one leg, lower your body until both
  knees are bent at 90 degrees, then step back<br />
  <strong>Modification:</strong> Hold onto a wall for balance
</p>

<h4>5. Plank Hold (40 seconds)</h4>
<p>
  <strong>Target:</strong> Core, shoulders, back<br />
  <strong>Form:</strong> Hold a straight-arm plank position, keeping your body
  in a straight line<br />
  <strong>Modification:</strong> Drop to your knees if needed
</p>

<h3>Complete Workout Flow</h3>

<p>
  <strong>Round 1:</strong> Push-ups → Rest 20s → Squats → Rest 20s → Mountain
  Climbers → Rest 20s → Lunges → Rest 20s → Plank → Rest 60s<br />
  <strong>Round 2:</strong> Repeat<br />
  <strong>Round 3:</strong> Repeat<br />
  <strong>Round 4:</strong> Repeat
</p>

<h3>Progression Tips</h3>

<p>As you get stronger, you can make this workout more challenging:</p>

<ul>
  <li>
    <strong>Add reps:</strong> Try to complete more reps in each 40-second window
  </li>
  <li><strong>Increase rounds:</strong> Add a 5th or 6th round</li>
  <li><strong>Reduce rest:</strong> Cut rest periods to 15 or 10 seconds</li>
  <li>
    <strong>Add variations:</strong> Try jump squats, burpees, or pike push-ups
  </li>
</ul>

